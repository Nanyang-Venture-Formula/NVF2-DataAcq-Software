bool get_r2d_seq_status(bool* isTriggered, bool* isDeactivated)
{
	bool r2d_sw_nc = HAL_GPIO_ReadPin(R2D_SW_NC_GPIOPORT, R2D_SW_NC_GPIOPIN);
	bool r2d_sw_no = HAL_GPIO_ReadPin(R2D_SW_NO_GPIOPORT, R2D_SW_NO_GPIOPIN);
	bool r2d_btn_no = HAL_GPIO_ReadPin(R2D_BTN_NC_GPIOPORT, R2D_BTN_NC_GPIOPIN);

	bool hasError = (r2d_sw_nc == r2d_sw_no);
	if (hasError)
	{
		&isDeactivated = false;
		&isTriggered = false;
		return false;
	}

//	condition is that NC is closed, NO is open
//	change logic here
	&isDeactivated = (r2d_sw_nc, !r2d_sw_no);

//	can only be triggered on 1 condition
//	change logic here
//	if is !isDeactivated AND button is pressed.
	&isTriggered = (!r2d_sw_nc && r2d_sw_no && !r2d_btn_no);

	return true;
}

bool report_r2d_status()
{
	uint8_t ret = 0;
	bool isTriggered = 0;
	bool isDeactivated = 0;
	bool hasError = get_r2d_seq_status(&isTriggered, &isDeactivated);

	ret |= (hasError << 3);
	ret |= (isDeactivated  << 2);
	ret |= (isTriggered << 1);

	txData[1] = ret;

//	DLC will always be 8, ts is sent
	app_can_tx(&hcan, &txHeader, txData, 8, txMailbox);
}

